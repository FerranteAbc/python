# Operadores aritiméticos 
adicao = 10 + 10
print("Adição", adicao)

subtracao = 10 - 5
print("Subtração", subtracao)

multiplicacao = 10 * 10
print("Multiplicação", multiplicacao)

divisao = 10 / 2.2   # Sempre retornará floot
print ("Divisão", divisao)

divisao_inteira = 10 // 2.2   # não retorna casas decimais (numero depois do ponto)
print("Divisão inteira", divisao_inteira)

exponenciacao = 2 ** 10  # potenciação (2 elevado há 10)
print('Exponenciação', exponenciacao)

modulo = 55 % 2   # da o resultado do resto da divisão  
print("Modulo", modulo)
print (10 % 8 == 0)  # mostra se 10 é divisível por 8 ou não
print (16 % 8 == 0)  # mostra se 16 é divisível por 16 ou não
print (10 % 2 == 0)  # mostra se é par ou impar 
print (16 % 2 == 0)  # mostra se é par ou impar
