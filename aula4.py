# Numros tipos int e float
#int numeros inteiros
# O tipo int representa qualquer numero
# positivo ou negativo. Sem sinal é considerado positivo
print(11) #int
print(-11) #int
print(0) #int

# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante
# Float sem sinal é considerado positivo
print(1.1, 10.11) #float - ultiliza o ponto para separar as casa decimais
print(0.0, -1.5) #float

# A função type mosta o tipo que o python inferiu ao valor
print(    type('Gaviões')      ) #tipo str - string ((tudo em phyton é um objeto ))
print(type(0)) #int
print(type(1.1), type(-1.1), type(0.0)) #float


