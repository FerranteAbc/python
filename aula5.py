# Tipo de dado bool (boolean)
# Ao questionar algo em um programa, so exitem duas respostas:
#(Sim (true)) ou (Não(False)) Ex. Você vai nadar hoje? (true) ou (False)
#Existem vários operadores para "questionar", dentre eles o (==).
#(==) é um operador lógico que questiona se um valor é igual a outro

print(10 == 10) # Sim -> true
print(10 == 11) # False -> false
print(type(True)) # retorna o tipo do dado. Esse é o bool
