# Operações condicionais 

# if /      elif           /    else
# se /    se não se     /      se não

# este codigo vai checar se a condição é verdadeira, como todas é falsas aparece o print do else
condicao = False
condicao2 = False
condicao3 = False
condicao4 = False

if condicao: 
    print('Codigo para condição')
elif condicao2:             # Comando pass (passa o codigo)
    print('Codigo para condição 2')
elif condicao3:
    print('Codigo para condição 3')
elif condicao4:
    print("Codigo para condição 4")
else:
    print('Nenhuma condição foi satisfeita')
   
   


